#include "r_cg_macrodriver.h"
#include "KeyHal.h"

/****************************************************************************
*                        Private Constant Definition                        *
****************************************************************************/
#define SETKEY_P P0.1
#define LAMPKEY_P P0.0

/****************************************************************************
*                              Public Function                              *
****************************************************************************/
TD_UB KeyHal_KeyStateRead(TD_UB aKey)
{
	switch (aKey)
	{
	case SETKEY:
		return (TD_UB)!SETKEY_P;
	case LAMPKEY:
		return (TD_UB)!LAMPKEY_P;
	}
}